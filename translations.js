window.t = (path) => {
    const lang = localStorage.getItem('enba_lang') || 'TR';
    const keys = path.split('.');
    let result = window.LANG_DATA[lang];
    
    for (const key of keys) {
        if (result && result[key]) {
            result = result[key];
        } else {
            console.warn(`Translation key not found: ${path} for language: ${lang}`);
            return path;
        }
    }
    return result;
};
